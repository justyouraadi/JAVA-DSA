package ExpressionBlock;

public class RelationalOperators {
    public static void main(String[] args) {
        //Relational Operators are used to check the realtionship between two operands.

        //Relational operators will return true or false;

        int a = 30, b = 40; 

        System.out.println(a+ " "+b);

        System.out.println(a == b);

        System.out.println(a != b);

        System.out.println(a > b);

        System.out.println(a >= b);

        System.out.println(a < b);

        System.out.println(a <= b);

        //Relational operators are used in the decision making process and loops.

    }
}
