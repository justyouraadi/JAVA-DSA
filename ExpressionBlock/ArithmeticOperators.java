package ExpressionBlock;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 100, b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
